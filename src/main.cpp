// Hardware constants
#define LED_PIN GPIO_NUM_27
#define GRID_WIDTH 64
#define GRID_HEIGHT 16
#define NUM_LEDS GRID_WIDTH * GRID_HEIGHT

// Software constants
#define USER_ID "1"
#define WIFI_SSID "maison"
#define WIFI_PASSWORD "maison609"
#define FLIGHT_URL "http://192.168.1.5:8080/flights"

SimpleWS2812B leds(LED_PIN, NUM_LEDS);
unsigned long last_fetch = 0;
unsigned long fetch_delay = 2500;
char* no_flights_text = "NONE";
char* flight_data = NULL;
char* flight_id = NULL;
char* flight_distance = NULL;
char* flight_altitude = NULL;

void init_wifi();
void set_led_color(int x, int y, LEDColor color);
int translate_coord(int x, int y);
void render_character(char code, int* x_pos, int* y_pos, LEDColor color);
void render_string(char* string, int* x_pos, int* y_pos, LEDColor color);
void fetch_flight();

// ESP32 functions

void setup() {
  Serial.begin(115200);

  init_wifi();
}

void loop() {
  if ((millis() - last_fetch) > fetch_delay) {
    fetch_flight();
    if (flight_id != NULL) {
      Serial.print("Flight ID:");
      Serial.println(flight_id);
    }
    if (flight_distance != NULL) {
      Serial.print("Distance:");
      Serial.println(flight_distance);
    }
    if (flight_altitude != NULL) {
      Serial.print("Altitude:");
      Serial.println(flight_altitude);
    }
  }

  int curr_x_pos = 0;
  int curr_y_pos = 0;

  leds.fill_color(LEDColor{0, 0, 0});

  render_string(flight_id, &curr_x_pos, &curr_y_pos, LEDColor{50, 50, 50});

  curr_x_pos = 0;
  curr_y_pos = 8;

  render_string(flight_distance, &curr_x_pos, &curr_y_pos, LEDColor{50, 50, 50});
  
  leds.update_leds();
  delay(1);
}

// Logic functions

void init_wifi() {
  WiFi.mode(WIFI_STA);
  WiFi.begin(WIFI_SSID, WIFI_PASSWORD);
  Serial.print("Connecting to WiFi ..");

  while (WiFi.status() != WL_CONNECTED) {
    Serial.print('.');
    delay(1000);
  }

  Serial.println(WiFi.localIP());
}

void render_string(char* string, int* x_pos, int* y_pos, LEDColor color) {
  for (int i = 0; i < strlen(string); i++) {
    render_character(string[i], x_pos, y_pos, color);
  }
}

void render_character(char code, int* x_pos, int* y_pos, LEDColor color) {
  if (code == ' ') {
    *x_pos += 8;
    return;
  }

  const char* char_data = FONT_8X8[code];

  for (int row = 0; row < 8; row++) {
    for (int col = 0; col < 8; col++) {
      int write = (char_data[row] >> col) & 1;

      if (write == 1) {
        set_led_color(*x_pos + col, *y_pos + row, color);
      } else {
        set_led_color(*x_pos + col, *y_pos + row, LEDColor{0, 0, 0});
      }
    }
  }

  *x_pos += 8;
}

void set_led_color(int x, int y, LEDColor color) {
  leds.set_color(translate_coord(x, y), color);
}

int translate_coord(int x, int y) {
  if (x % 2 == 0) {
    return (GRID_HEIGHT * x) + y;
  } else {
    return (GRID_HEIGHT * (x + 1)) - (y + 1);
  }
}

void fetch_flight() {
  if (WiFi.status() != WL_CONNECTED) {
    Serial.println("Could not fetch flight data due to no WiFi");
    return;
  }

  HTTPClient http;
  http.begin(FLIGHT_URL);
  http.addHeader("X-User-Id", USER_ID);
  int response_code = http.GET();

  if (response_code > 0) {
    if (response_code == 200) {
      const char* payload = http.getString().c_str();
      Serial.print("Successfully fetch flight data: ");
      Serial.println(payload);

      if (flight_data != NULL) {
        free(flight_data);
        flight_data = NULL;
        flight_id = NULL;
        flight_distance = NULL;
        flight_altitude = NULL;
      }

      flight_data = (char*)malloc(strlen(payload) + 1); 
      strcpy(flight_data, payload);

      char* token = strtok(flight_data, ":");
      int index = 0;

      // FLIGHT_ID:DISTANCE:ALTITUDE

      while (token != NULL) {
        if (index == 0) {
          flight_id = token;
        } else if (index == 1) {
          flight_distance = token;
        } else if (index == 2) {
          flight_altitude = token;
        }

        token = strtok(NULL, ":");
        index++;
      }
    } else {
      flight_id = no_flights_text;
    }
  } else {
    Serial.print("Failed to fetch flight data: ");
    Serial.println(response_code);
  }

  http.end();
  last_fetch = millis();
}
